#include "libft.h"

/*
** Checks if is part of the ASCII alphabet
*/

int		ft_isalpha(int c)
{
	if (('a' <= c && 'z' >= c) || ('A' <= c && 'Z' >= c))
		return (1);
	return (0);
}
